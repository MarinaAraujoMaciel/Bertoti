    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome, raca, idade, banho, tosa, vacina;

        nome = txtNome.getText();
        raca = txtRaca.getText();
        idade = txtIdade.getText();
        banho = txtBanho.getText();
        tosa = txtTosa.getText();
        vacina = txtVacina.getText();
        
        System.out.println(banho.trim().length());
        
        if (banho.trim().length() < 11){
            banho = "Sem banho registrado"; 
        }else{
        }   
         if (tosa.trim().length() < 11){
            tosa = "Sem tosa registrada"; 
        }else{
        } 

        petsDTO objpetsDTO = new petsDTO();
        objpetsDTO.setNome(nome);
        objpetsDTO.setRaca(raca);
        objpetsDTO.setIdade(idade);
        objpetsDTO.setBanho(banho);
        objpetsDTO.setTosa(tosa);
        objpetsDTO.setVacina(vacina);
        
        petsDAO objpet = new petsDAO();
        objpet.NovoPet(objpetsDTO);

        JOptionPane.showMessageDialog(null, "Cadastro feito!");

        dispose();

        Tabela objListausergui = new Tabela();
        objListausergui.setVisible(true);  

        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

