public class Roupa {
    private int tamanho;
    private String durabilidade;
    private String marca;
    private String cor;
    private double valor;

    public int getTamanho() {
        return tamanho;
    }

    public String getDurabilidade() {
        return durabilidade;
    }

    public String getMarca() {
        return marca;
    }

    public String getCor() {
        return cor;
    }

    public double getValor() {
        return valor;
    }
}